
//====================================================================
// a[N][M] x b[M][N] = c[N][N]
//====================================================================
void mat_mul(float *a, float *b, float *c, int N, int M) {
  for (int i = 0; i < N; i++) {
    for (int j = 0; j < N; j++) {
      for (int k = 0; k < M; k++) {
        c[i * N + j] += a[i * M + k] * b[k * N + j];
      }
    }
  }
}
